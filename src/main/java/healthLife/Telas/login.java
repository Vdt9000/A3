    private void btn_entrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_entrarActionPerformed
        // TODO add your handling code here:
        
        String cpf = txt_login.getText();
        String senha = new String (txt_senha.getPassword()); 
        boolean Emedico = false;
       
       try{
            DAOMédico dao2 = new DAOMédico();
            Médico med = new Médico(cpf, senha);
           
            if(dao2.login(med)){
                JOptionPane.showMessageDialog(null, "Seja bem vindo ao Health Life");
                Emedico = true;
                adm_med tu = new adm_med();
                tu.setVisible(true);
                this.dispose();  
            }
        }catch(Exception e){
                JOptionPane.showMessageDialog(null, " ERRO"); 
                
        }
        
        if(Emedico == false){
            try{
                DAOPaciente dao = new DAOPaciente();
                Paciente paciente = new Paciente(cpf, senha);
                if (dao.login(paciente)){
                    JOptionPane.showMessageDialog(null, "Seja bem vindo ao Health Life");
                    tela_paciente tu = new tela_paciente();
                    tu.setVisible(true);
                    this.dispose();       
                 }
                else{
                    JOptionPane.showMessageDialog(null, " Desculpe, mas este usuário não encontrado :(");
                } 
            }
        catch(Exception ex){
                JOptionPane.showMessageDialog(null, " ERRO");      
        }
        }
                
           
    }//GEN-LAST:event_btn_entrarActionPerformed

